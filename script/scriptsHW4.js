let chisloTovarov = prompt("Введите число-товаров", 0);
function getNoun(chisloTovarov, товар, товара, товаров) {
    let nv = chisloTovarov;
    let last = nv.toString().slice(-1);
    if (last >= 5 && last <= 20) {
        return товаров;
    }
    if (last == 1) {
        return товар;
    }
    if (last >= 2 && last <= 4) {
        return товара;
    }
    return товаров;
}
